using System;
using System.Collections.Generic;
using System.Text;

namespace Trenins
{
    class Matematika
    {
        public double Saskaitit(double a, double b)
        {
            double Summa = a + b;
            return Summa;
        }

        public double Atnemt(double a, double b)
        {
            double Starpiba = a - b;
            return Starpiba;

        }

        public double Reizinat(double a, double b)
        {
            double Reizinajums = a * b;
            return Reizinajums;
        }

        public double Dalit(double a, double b)
        {
            double Dalijums = a / b;
            return Dalijums;
        }

    }
}
